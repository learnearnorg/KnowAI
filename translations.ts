/**
 * A helper to process templates like "Hello {0}" -> "Hello World"
 */
export const interpolate = (str: string, ...args: (string | number)[]) => {
  return str.replace(/{(\d+)}/g, (match, number) => {
    return typeof args[number] !== 'undefined' ? String(args[number]) : match;
  });
};

/**
 * Returns a version of the translation object that handles function-like interpolation 
 * but behaves like the original TRANSLATIONS structure.
 */
export const getActiveTranslations = (lang: LanguageCode, overrides: Record<string, string> = {}) => {
  const base = TRANSLATIONS_RAW[lang] || BASE_EN;
  const merged = { ...base, ...overrides };
  
  // We recreate the structure expected by the app (functions for interpolated strings)
  return {
    ...merged,
    welcomeMessage: (group: string) => interpolate(merged.welcomeMessage, group),
    maxUrlsError: (max: number) => interpolate(merged.maxUrlsError, max),
    maxUrlsReached: (max: number) => interpolate(merged.maxUrlsReached, max),
    noUrlsHelp: (group: string) => interpolate(merged.noUrlsHelp, group),
  } as any;
};

